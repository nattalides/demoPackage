#' Add Two Numbers
#'
#' The purpose of this function is to take two numbers and add them
#' \code{add_two_numbers} is an awesome function!
#'
#' @param a (numeric) A positive number
#' @param b (numeric) A positive number
#'
#' @return The total of the two number supplied
#' @export
#'
#' @examples
#' add_two_numbers(a = 1, b = 1)
add_two_numbers <- function(a,b) {

    total <- a + b

    return(total)

}
